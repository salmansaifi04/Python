# check two condition at same time

# and (overall condition is satisfy then if block is execute)
name = "salman"
age = 21
if name == "salman" and age == 21:
    print("condition true")
else:
    print("condition false")


# or  (at least one condition is satisfy then if block is execute)
if name == "salman" or age == 23:
    print("condition true")
else:
    print("condition false")