# we can easily change the datatype of a variable
name = 123
print(name) 


############# rules for creating a variable ##############
# 1. cannot start with a number 
# 1num = 123    # it give error

# 2. we cannot use special symbol (!,@,#,%,&)
# $usd = 34     # it gives error
# us$d = 23     # it gives error

# 3. we can start variable with a "_", any letter 
# num = 123     # no error
# _number = 1234    # no error
# nu2m = 234        # no error 


###### naming convention for variable ############

