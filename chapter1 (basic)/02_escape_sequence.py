# escape sequence

'''
\' ---> single quote
\" ---> double quote
\\ ---> backslash
\n ---> newline
\t ---> tab
\b ---> backspace 
'''

print("hello \"world\"")

print('hello \'world\'')

print("line1\nline2")   # it is used for new line

print("line1\tline2")   # it is used for tab space

print("line1\bline2")   # it is used for backspace

print("this is a \\ single backslah")  # it is used for backslash